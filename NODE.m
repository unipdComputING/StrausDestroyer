classdef NODE
  %------------------------------------------------------------------------
  properties(Constant)
    draw_diameter = 0.05;
    draw_linewidth = 1.0;
  end
  %------------------------------------------------------------------------
  properties
    id;
    x=[];
  end
  %------------------------------------------------------------------------
  methods
    %----------------------------------------------------------------------
    function this = NODE(id, X)
      this.id = id;
      this.x = X;
    end
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    function write(this)
      fprintf('NODE ID: %i; ',this.id);
      fprintf('X = [%f,%f,%f]^T\n',this.x(1),this.x(2),this.x(3));
    end
    %----------------------------------------------------------------------
    function draw(this)
      d = this.draw_diameter;
      rectangle('position', [this.x(1)-0.5*d,this.x(2)-0.5*d,d,d], ...
                'Curvature',[1 1], ...
                'FaceColor','g', ...
                'EdgeColor','k', ...
                'LineWidth',this.draw_linewidth);
      %    
    end
    %----------------------------------------------------------------------
  end
  %------------------------------------------------------------------------
end