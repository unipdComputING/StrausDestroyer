classdef PROPERTY < handle
  %------------------------------------------------------------------------
  properties
    id;
    mat;
    sec;
  end
  %------------------------------------------------------------------------
  methods
    %----------------------------------------------------------------------
    function this=PROPERTY(id, material, section)
      this.id = id;
      if (nargin >= 2)
        this.mat = material;
      end
      if (nargin >= 3)
        this.sec = section;
      end
    end
    %----------------------------------------------------------------------
    function setSection(this, section)
      this.sec = section;
    end
    %----------------------------------------------------------------------
    function setMaterial(this, material)
      this.mat = material;
    end
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    function write(this)
      fprintf('ID PROP: %i\n', this.id);
      try
        this.mat.write();
      catch
        fprintf("--Material: WARNING material not defined\n")
      end
      
      try
        this.sec.write();
      catch
        fprintf("--Section: WARNING section not defined\n")
      end
      fprintf("\n");
    end
    %----------------------------------------------------------------------
  end
  %------------------------------------------------------------------------
end